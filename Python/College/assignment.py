a = int(input('Enter Number : '))
b = int(input('Enter Number : '))

print('Sum : ', a + b)
print('Sub : ', a - b)
print('Mul : ', a * b)
print('Div : ', a / b)
print('Mod : ', a % b)
print('Pow : ', a ** b)
print('Floor : ', a // b)

print('Equal : ', a == b)
print('Great : ', a > b)
print('Less : ', a < b)
print('GreatEq : ', a >= b)
print('LessEq : ', a <= b)
print('Not Equal : ', a != b)

print('& : ', a & b)
print('| : ', a | b)
print('~a : ', ~a)
print('~b : ', ~b)
print('a^b : ', a^b)
print('a << 1 : ', a<<1)
print('b >> 1: ', b >>1)


